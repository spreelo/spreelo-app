export function normalizeUiLocale(value) {
  const rawLocale = String(value || "").trim().toLowerCase();

  if (!rawLocale) return DEFAULT_UI_LOCALE;

  const localeAliases = {
    se: "sv",
    dk: "da",
    no: "no",
    nb: "no",
    nn: "no",
    ua: "uk",
    jp: "ja",
    cn: "zh",
    gr: "el",
  };

  const locale = rawLocale.replace("_", "-");
  const aliasedLocale = localeAliases[locale] || locale;
  const primaryLanguage = aliasedLocale.split("-")[0];
  const shortLocale = localeAliases[primaryLanguage] || primaryLanguage;

  const exactMatch = SUPPORTED_UI_LOCALES.find(
    (item) => item.locale.toLowerCase() === aliasedLocale
  );

  if (exactMatch) return exactMatch.locale;

  const shortMatch = SUPPORTED_UI_LOCALES.find(
    (item) => item.locale.toLowerCase() === shortLocale
  );

  if (shortMatch) return shortMatch.locale;

  if (!/^[a-z]{2,3}$/.test(shortLocale)) {
    return DEFAULT_UI_LOCALE;
  }

  try {
    const displayNames = new Intl.DisplayNames(["en"], { type: "language" });
    const languageName = displayNames.of(shortLocale);

    if (
      languageName &&
      languageName.toLowerCase() !== shortLocale.toLowerCase()
    ) {
      return shortLocale;
    }
  } catch {
    return DEFAULT_UI_LOCALE;
  }

  return DEFAULT_UI_LOCALE;
}

export function getSupportedUiLocale(locale) {
  const normalizedLocale = normalizeUiLocale(locale);

  return (
    SUPPORTED_UI_LOCALES.find((item) => item.locale === normalizedLocale) || null
  );
}

export function getUiNativeLanguageName(locale) {
  const match = getSupportedUiLocale(locale);

  return match?.nativeName || match?.language || normalizeUiLocale(locale);
}

export function getUiLanguageName(locale) {
  const normalizedLocale = normalizeUiLocale(locale);
  const match = getSupportedUiLocale(normalizedLocale);

  if (match?.language) return match.language;

  try {
    const displayNames = new Intl.DisplayNames(["en"], { type: "language" });
    return displayNames.of(normalizedLocale) || normalizedLocale;
  } catch {
    return normalizedLocale || "English";
  }
}

export function getDefaultNamespaceLabels(namespace) {
  return DEFAULT_UI_LABELS[namespace] || {};
}

export function getDefaultLabelsForNamespaces(namespaces = []) {
  return namespaces.reduce((labels, namespace) => {
    return {
      ...labels,
      ...getDefaultNamespaceLabels(namespace),
    };
  }, {});
}

export function interpolateUiText(text, values = {}) {
  return String(text || "").replace(/\{(\w+)\}/g, (match, key) => {
    const value = values?.[key];

    if (value === null || value === undefined) {
      return match;
    }

    return String(value);
  });
}
